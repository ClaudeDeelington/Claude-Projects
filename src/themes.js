export function applyTheme(themeName) {
  const theme = THEME_DEFINITIONS[themeName]
  if (!theme) return
  const root = document.documentElement
  Object.entries(theme).forEach(([key, value]) => {
    if (key.startsWith('--')) root.style.setProperty(key, value)
  })
}
